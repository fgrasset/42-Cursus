# include <unistd.h>

int	main(int ac, char **ag)
{
	if (ac == 2)
	{
		int	i = 0;

		while (ag[1][i] && ((ag[1][i] == ' ') || (ag[1][i] == '\t')))
		{
			i++;
		}
		while(ag[1][i])
		{
			if (ag[1][i] == ' ' || ag[1][i] == '\t')
				break ;
			write(1, &ag[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
